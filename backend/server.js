function generateGatheringTasks(payload) {
  const { gatheringType, guestCount, date: eventDateStr, time: eventTimeStr, gatheringId } = payload;
  const eventDate = new Date(`${eventDateStr}T${String(eventTimeStr).slice(0, 5)}:00`);
  const eventDateOnly = eventDateStr.slice(0, 10);
  const today = new Date();
  today.setHours(0, 0, 0, 0);
  const eventDay = new Date(eventDateOnly);
  eventDay.setHours(0, 0, 0, 0);
  const daysUntil = Math.round((eventDay - today) / (24 * 60 * 60 * 1000));
  const isLunch = gatheringType === "lunch";
  const gateTime = isLunch ? "14:00" : "19:00";
  const gateTask = { title: "Keep the gate open", category: "hosting", duration: 10, time: gateTime, date: eventDateOnly };

  const room45 = ROOM_PREP_45.map((title) => ({ title, duration: 45, category: "room" }));
  const room60 = ROOM_PREP_60.map((title) => ({ title, duration: 60, category: "room" }));
  const driveway = daysUntil >= 7 ? [{ title: "Driveway", category: "room", duration: DRIVEWAY_DURATION }] : [];
  const allRoom = [...room45, ...room60, ...driveway];
  const allOther = [...OTHER_TASKS, gateTask];

  const tasksToPlace = [
    ...allRoom.map((t) => ({ ...t, timeWindow: null })),
    ...allOther.map((t) => ({ ...t, timeWindow: t.time ? null : null, time: t.time, dateOverride: t.date })),
  ];

  const gid = gatheringId || nextId();
  const created = [];
  const windows = ["morning", "afternoon", "evening"];

  function placeTask(taskSpec, dateStr, windowKey) {
    const bounds = getSlotBounds(dateStr, windowKey);
    if (!bounds) return null;
    const dur = taskSpec.duration || 60;
    const existingOnDay = taskStore.filter((t) => t.date === dateStr).map(toNormalizedTask).filter(Boolean);
    let cursor = bounds.start.getTime();
    const endTs = bounds.end.getTime();
    while (cursor + dur * 60 * 1000 <= endTs) {
      const startIso = new Date(cursor).toISOString();
      const endIso = new Date(cursor + dur * 60 * 1000).toISOString();
      const overlaps = existingOnDay.some((t) => {
        const tStart = new Date(t.startTime).getTime();
        const tEnd = new Date(t.endTime).getTime();
        return cursor < tEnd && cursor + dur * 60 * 1000 > tStart;
      });
      if (!overlaps) {
        const task = {
          id: nextId(),
          title: taskSpec.title,
          date: dateStr,
          startTime: startIso,
          endTime: endIso,
          durationMinutes: dur,
          status: "pending",
          category: taskSpec.category || "prep",
          gatheringId: gid,
          isAutoGenerated: true,
        };
        taskStore.push(task);
        created.push(toNormalizedTask(task));
        existingOnDay.push(toNormalizedTask(task));
        return task;
      }
      cursor += dur * 60 * 1000;
    }
    return null;
  }

  if (daysUntil === 1) {
    const todayStr = today.toISOString().slice(0, 10);
    let wi = 0;
    for (const t of allRoom) {
      const w = windows[wi % 3];
      placeTask(t, todayStr, w);
      wi++;
    }
    for (const t of allOther) {
      if (t.dateOverride && t.time) {
        const startIso = `${t.dateOverride}T${t.time}:00`;
        const d = new Date(startIso);
        const endIso = new Date(d.getTime() + (t.duration || 10) * 60 * 1000).toISOString();
        const task = {
          id: nextId(),
          title: t.title,
          date: t.dateOverride,
          startTime: startIso,
          endTime: endIso,
          durationMinutes: t.duration || 10,
          status: "pending",
          category: t.category || "hosting",
          gatheringId: gid,
          isAutoGenerated: true,
        };
        taskStore.push(task);
        created.push(toNormalizedTask(task));
      } else {
        const w = windows[wi % 3];
        placeTask(t, todayStr, w);
        wi++;
      }
    }
  } else if (daysUntil >= 2 && daysUntil <= 6) {
    const prepTasks = [...allRoom, ...allOther.filter((x) => !x.dateOverride)];
    const availableDays = [];
    for (let d = 1; d < daysUntil; d++) {
      const slotDate = new Date(today);
      slotDate.setDate(slotDate.getDate() + d);
      availableDays.push(slotDate.toISOString().slice(0, 10));
    }
    let taskIdx = 0;
    outer: for (const dateStr of availableDays) {
      for (const w of windows) {
        while (taskIdx < prepTasks.length) {
          if (placeTask(prepTasks[taskIdx], dateStr, w)) {
            taskIdx++;
            continue;
          }
          break;
        }
        if (taskIdx >= prepTasks.length) break outer;
      }
    }
    for (const t of allOther) {
      if (t.dateOverride && t.time) {
        const startIso = `${t.dateOverride}T${t.time}:00`;
        const endIso = new Date(new Date(startIso).getTime() + (t.duration || 10) * 60 * 1000).toISOString();
        const task = {
          id: nextId(),
          title: t.title,
          date: t.dateOverride,
          startTime: startIso,
          endTime: endIso,
          durationMinutes: t.duration || 10,
          status: "pending",
          category: t.category || "hosting",
          gatheringId: gid,
          isAutoGenerated: true,
        };
        taskStore.push(task);
        created.push(toNormalizedTask(task));
      }
    }
  } else if (daysUntil >= 7) {
    const prepTasks = [...allRoom, ...allOther.filter((x) => !x.dateOverride)];
    let taskIdx = 0;
    outer7: for (let d = 1; d < daysUntil; d++) {
      const slotDate = new Date(today);
      slotDate.setDate(slotDate.getDate() + d);
      const dateStr = slotDate.toISOString().slice(0, 10);
      for (const w of windows) {
        while (taskIdx < prepTasks.length) {
          if (placeTask(prepTasks[taskIdx], dateStr, w)) {
            taskIdx++;
            continue;
          }
          break;
        }
        if (taskIdx >= prepTasks.length) break outer7;
      }
    }
    for (const t of allOther) {
      if (t.dateOverride && t.time) {
        const startIso = `${t.dateOverride}T${t.time}:00`;
        const endIso = new Date(new Date(startIso).getTime() + (t.duration || 10) * 60 * 1000).toISOString();
        const task = {
          id: nextId(),
          title: t.title,
          date: t.dateOverride,
          startTime: startIso,
          endTime: endIso,
          durationMinutes: t.duration || 10,
          status: "pending",
          category: t.category || "hosting",
          gatheringId: gid,
          isAutoGenerated: true,
        };
        taskStore.push(task);
        created.push(toNormalizedTask(task));
      }
    }
  }

  return { gatheringId: gid, created };
}

