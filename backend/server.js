const TIME_REGEX = /^([01]?\d|2[0-3]):([0-5]\d)(?::([0-5]\d))?$/;
const DATE_REGEX = /^\d{4}-\d{2}-\d{2}$/;
function validTime(s) {
  return typeof s === "string" && TIME_REGEX.test(s.trim());
}
function validDate(s) {
  return typeof s === "string" && DATE_REGEX.test(s.trim()) && !isNaN(new Date(s + "T12:00:00Z").getTime());
}
function validRecurrence(s) {
  return s === "none" || s === "daily" || s === "weekly";
}

