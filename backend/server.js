fastify.patch("/urgent_tasks/:id", async (request, reply) => {
  try {
    const { id } = request.params;
    const body = request.body || {};

    const fields = [];
    const values = [];
    let i = 1;

    if (typeof body.acknowledged === "boolean") {
      fields.push(`acknowledged = $${i++}`);
      values.push(body.acknowledged);
    }
    if (typeof body.title === "string" && body.title.trim()) {
      fields.push(`title = $${i++}`);
      values.push(body.title.trim());
    }

    if (fields.length === 0) {
      return sendError(reply, 400, "Invalid body");
    }

    values.push(id);
    const { rows } = await db.query(
      `UPDATE urgent_tasks SET ${fields.join(", ")} WHERE id = $${i} RETURNING *`,
      values
    );
    if (!rows[0]) return sendError(reply, 404, "Urgent task not found");
    return reply.send(rows[0]);
  } catch (e) {
    request.log.error({ err: safeErrorMessage(e) }, "error");
    return sendError(reply, 500, safeErrorMessage(e));
  }
});
