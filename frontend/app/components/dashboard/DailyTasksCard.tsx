type Task = {
  id: string;
  title: string;
  date: string;
  startTime: string;
  endTime?: string;
  durationMinutes?: number;
  status: "pending" | "completed";
  category?: string;
  gatheringId?: string | null;
  isAutoGenerated?: boolean;
  /** Stable display order. Required for correct render order; never sort by startTime. */
  order: number;
};

function formatDateDisplay(dateStr: string): string {
  return new Date(dateStr + "T12:00:00").toLocaleDateString(undefined, {
    weekday: "short",
    month: "short",
    day: "numeric",
  });
}

function timeWindowLabel(iso: string): string {
  const h = new Date(iso).getHours();
  if (h < 12) return "Morning";
  if (h < 17) return "Afternoon";
  return "Evening";
}

function isToday(dateStr: string): boolean {
  const today = new Date().toISOString().slice(0, 10);
  return dateStr === today;
}

/** Only used when loading from API to assign initial order. Do not use for display. */
function sortTodayTasks(tasks: Task[]): Task[] {
  return [...tasks].sort((a, b) => {
    if (a.status === "completed" && b.status !== "completed") return 1;
    if (a.status !== "completed" && b.status === "completed") return -1;
    return new Date(a.startTime).getTime() - new Date(b.startTime).getTime();
  });
}

/** Only sort by order. NO other sorting. Used as single source for render. */
function tasksByOrder(tasks: Task[]): Task[] {
  return [...tasks].sort((a, b) => a.order - b.order);
}

// Sortable task row (incomplete only)
function SortableTaskRow({
  task,
  isHolding,
  taskCardStyle,
  onPressStart,
  onPressEnd,
  onClick,
  onContextMenu,
  readOnly,
  canReorder,
  simplified,
}: {
  task: Task;
  isHolding: boolean;
  taskCardStyle: React.CSSProperties;
  onPressStart: (e: React.MouseEvent | React.TouchEvent) => void;
  onPressEnd: () => void;
  onClick: (e: React.MouseEvent) => void;
  onContextMenu: (e: React.MouseEvent) => void;
  readOnly?: boolean;
  canReorder?: boolean;
  simplified?: boolean;
}) {
  const { attributes, listeners, setNodeRef, transform, transition, isDragging } = useSortable({ id: task.id });
  const style: React.CSSProperties = {
    ...taskCardStyle,
    opacity: task.status === "completed" ? 0.4 : 1,
    transform: isDragging ? undefined : isHolding ? "scale(0.97)" : task.status === "completed" ? "scale(0.98)" : undefined,
    boxShadow: isHolding ? "0 0 20px rgba(120,180,255,0.25)" : undefined,
    ...(transform ? { transform: CSS.Transform.toString(transform) } : {}),
    transition,
    maxWidth: "100%",
  };
  return (
    <li ref={setNodeRef} style={style} className="min-w-0">
      <button
        type="button"
        onClick={(e) => onClick(e)}
        onMouseDown={(e) => !readOnly && onPressStart(e)}
        onMouseUp={onPressEnd}
        onTouchStart={(e) => !readOnly && onPressStart(e)}
        onTouchEnd={onPressEnd}
        onContextMenu={onContextMenu}
        className={`w-full flex items-center gap-4 px-4 text-left rounded-[28px] border border-white/[0.12] backdrop-blur-[18px] transition-all duration-300 ease-out hover:shadow-[0_0_20px_rgba(120,180,255,0.2)] ${simplified ? "py-4 min-h-[52px]" : "py-3"}`}
        onMouseEnter={(e) => {
          if (task.status !== "completed" && !isHolding) e.currentTarget.style.boxShadow = "0 0 20px rgba(120,180,255,0.2)";
        }}
        onMouseLeave={(e) => {
          onPressEnd();
          if (!isHolding) e.currentTarget.style.boxShadow = "";
        }}
      >
        <span className="shrink-0 text-xs tabular-nums opacity-70 min-w-[7rem]">
          {formatTaskTimeRange(task.startTime, task.endTime ?? new Date(new Date(task.startTime).getTime() + (task.durationMinutes ?? 60) * 60000).toISOString(), task.durationMinutes)}
        </span>
        <span className={`min-w-0 flex-1 truncate text-[0.875rem] text-white/90 ${task.status === "completed" ? "line-through" : ""}`}>{task.title}</span>
        {task.status !== "completed" && !readOnly && canReorder && (
          <span
            className="drag-handle shrink-0 text-white/40 cursor-grab active:cursor-grabbing select-none"
            style={{ touchAction: "none" }}
            aria-hidden
            {...attributes}
            {...listeners}
          >
            ⋮⋮
          </span>
        )}
      </button>
    </li>
  );
}

