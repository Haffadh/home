type DailyTasksCardProps = { readOnly?: boolean; canEditTasks?: boolean; canReorder?: boolean; simplified?: boolean };

export default function DailyTasksCard({ readOnly = false, canEditTasks = true, canReorder = true, simplified = false }: DailyTasksCardProps = {}) {
  const [tasks, setTasks] = useState<Task[]>([]);
  const [loading, setLoading] = useState(true);
  const [selectedTask, setSelectedTask] = useState<Task | null>(null);
  const [skipTaskModal, setSkipTaskModal] = useState<Task | null>(null);
  const [replacePicker, setReplacePicker] = useState(false);
  const [editTask, setEditTask] = useState<Task | null>(null);
  const [howToTitle, setHowToTitle] = useState<string | null>(null);
  const [isLongPress, setIsLongPress] = useState(false);
  const [holdingTaskId, setHoldingTaskId] = useState<string | null>(null);
  const pressTimerRef = useRef<ReturnType<typeof setTimeout> | null>(null);
  const pendingLongPressTaskRef = useRef<Task | null>(null);
  const tasksRef = useRef<Task[]>(tasks);
  useEffect(() => {
    tasksRef.current = tasks;
  }, [tasks]);

  const sensors = useSensors(
    useSensor(PointerSensor, { activationConstraint: { delay: 150, tolerance: 5 } }),
    useSensor(KeyboardSensor, { coordinateGetter: sortableKeyboardCoordinates })
  );

  const loadTasks = useCallback(async () => {
    setLoading(true);
    try {
      const rows = await tasksService.fetchTasksFromApi();
      const ui = taskRowsToUI(rows ?? []);
      const merged = getMergedTasks(ui as Task[]);
      const todayOnly = merged.filter((t) => isToday(t.date)) as Task[];
      const sorted = sortTodayTasks(todayOnly).slice(0, 6);
      setTasks(sorted.map((t, i) => ({ ...t, order: i })));
    } catch {
      setTasks([]);
    } finally {
      setLoading(false);
    }
  }, []);

  useEffect(() => {
    const onMidnight = () => loadTasks();
    const onGatheringAdded = () => loadTasks();
    window.addEventListener("midnight-rollover", onMidnight);
    window.addEventListener("gathering-tasks-added", onGatheringAdded);
    return () => {
      window.removeEventListener("midnight-rollover", onMidnight);
      window.removeEventListener("gathering-tasks-added", onGatheringAdded);
    };
  }, [loadTasks]);

  useEffect(() => {
    loadTasks();
  }, [loadTasks]);

  useRealtimeTable("tasks", loadTasks);

  useEffect(() => {
    return () => {
      if (pressTimerRef.current) clearTimeout(pressTimerRef.current);
    };
  }, []);

  function handlePressStart(task: Task, e: React.MouseEvent | React.TouchEvent) {
    if (readOnly) return;
    if (e.target instanceof HTMLElement && e.target.closest(".drag-handle")) return;
    setIsLongPress(false);
    setHoldingTaskId(task.id);
    pendingLongPressTaskRef.current = task;
    if (pressTimerRef.current) clearTimeout(pressTimerRef.current);
    pressTimerRef.current = setTimeout(() => {
      setIsLongPress(true);
      const t = pendingLongPressTaskRef.current;
      if (t) setSelectedTask(t);
      pressTimerRef.current = null;
    }, LONG_PRESS_MS);
  }

  function handlePressEnd() {
    if (pressTimerRef.current) {
      clearTimeout(pressTimerRef.current);
      pressTimerRef.current = null;
    }
    pendingLongPressTaskRef.current = null;
    setHoldingTaskId(null);
  }

  function handleTaskClick(task: Task, e: React.MouseEvent) {
    if (readOnly) return;
    if (e.target instanceof HTMLElement && e.target.closest(".drag-handle")) return;
    if (isLongPress) return;
    toggleTask(task);
  }

  async function toggleTask(task: Task) {
    const nextStatus: Task["status"] = task.status === "completed" ? "pending" : "completed";
    const prevTasks = tasks;
    setTasks((current) => {
      const next = current.map((t) => (t.id === task.id ? { ...t, status: nextStatus } : t));
      if (nextStatus === "completed") {
        const maxOrder = Math.max(-1, ...next.filter((x) => x.status === "completed").map((x) => x.order));
        return next.map((t) => (t.id === task.id ? { ...t, status: "completed" as const, order: maxOrder + 1 } : t));
      }
      const maxOrder = Math.max(-1, ...next.filter((x) => x.status !== "completed").map((x) => x.order));
      return next.map((t) => (t.id === task.id ? { ...t, order: maxOrder + 1 } : t));
    });
    try {
      if (getSupabaseClient()) {
        if (nextStatus === "completed") {
          await tasksService.completeTask(task.id);
        } else {
          await tasksService.updateTask(task.id, { status: "pending" });
        }
      }
    } catch {
      setTasks(prevTasks);
      loadTasks();
    }
  }

  function openSkipModal(task: Task) {
    setSelectedTask(null);
    setIsLongPress(false);
    setSkipTaskModal(task);
    setReplacePicker(false);
  }

  function taskToRowLike(t: Task): TaskRow {
    const end = t.endTime ?? new Date(new Date(t.startTime).getTime() + (t.durationMinutes ?? 60) * 60000).toISOString();
    return {
      id: t.id,
      title: t.title,
      assigned_by: null,
      start_time: t.startTime,
      end_time: end,
      duration: t.durationMinutes ?? 60,
      urgent: false,
      room: null,
      status: t.status,
      created_at: new Date().toISOString(),
    };
  }

  async function handleSkipChoice(option: SkipOption) {
    const task = skipTaskModal;
    if (!task || !getSupabaseClient()) return;
    if (option === "leave_empty") {
      try {
        await tasksService.deleteTask(task.id);
        await loadTasks();
      } catch {
        loadTasks();
      }
      setSkipTaskModal(null);
      return;
    }
    if (option === "replace") {
      setReplacePicker(true);
      return;
    }
    if (option === "bring_forward") {
      try {
        const today = new Date().toISOString().slice(0, 10);
        const rows = await tasksService.fetchTasks({ date: today });
        const pending = rows.filter((r) => r.status !== "completed").sort((a, b) => new Date(a.start_time).getTime() - new Date(b.start_time).getTime());
        const idx = pending.findIndex((r) => r.id === task.id);
        if (idx === -1) {
          await tasksService.deleteTask(task.id);
          await loadTasks();
          setSkipTaskModal(null);
          return;
        }
        const durationMs = (task.durationMinutes ?? 60) * 60 * 1000;
        await applyShiftTasksBackward(pending, idx + 1, durationMs);
        await tasksService.deleteTask(task.id);
        await loadTasks();
      } catch {
        loadTasks();
      }
      setSkipTaskModal(null);
    }
  }

  async function handleReplaceWith(other: Task) {
    const task = skipTaskModal;
    if (!task || !getSupabaseClient()) return;
    try {
      const rowA = taskToRowLike(task);
      const rowB = taskToRowLike(other);
      await applySwapTaskSlots(rowA, rowB);
      await loadTasks();
    } catch {
      loadTasks();
    }
    setSkipTaskModal(null);
    setReplacePicker(false);
  }

  async function handleEditSave(
    task: Task,
    payload: { title: string; date: string; startTime: string; durationMinutes: number }
  ) {
    if (!getSupabaseClient()) return;
    const [h, m] = payload.startTime.split(":").map(Number);
    const startDate = new Date(payload.date);
    startDate.setHours(h, m, 0, 0);
    const startIso = startDate.toISOString();
    const endIso = new Date(startDate.getTime() + payload.durationMinutes * 60 * 1000).toISOString();
    try {
      await tasksService.updateTask(task.id, {
        title: payload.title,
        start_time: startIso,
        end_time: endIso,
        duration: payload.durationMinutes,
      });
    } catch {
      // ignore
    }
    setEditTask(null);
    setSelectedTask(null);
    await loadTasks();
  }

  function handleHowTo(task: Task) {
    setHowToTitle(task.title);
    setSelectedTask(null);
    setIsLongPress(false);
  }

  function handleDragStart() {
    document.body.style.overflowX = "hidden";
  }

  /** Reorder = swap time slots of the two tasks (active and over). */
  async function handleDragEnd(event: DragEndEvent) {
    document.body.style.overflowX = "";
    if (!canReorder) return;
    const { active, over } = event;
    if (!over || String(active.id) === String(over.id)) return;
    const activeId = String(active.id);
    const overId = String(over.id);
    const prev = tasksRef.current;
    const incomplete = prev.filter((t) => t.status !== "completed");
    const activeTask = incomplete.find((t) => t.id === activeId);
    const overTask = incomplete.find((t) => t.id === overId);
    if (!activeTask || !overTask || !getSupabaseClient()) return;
    try {
      await applySwapTaskSlots(taskToRowLike(activeTask), taskToRowLike(overTask));
      await loadTasks();
    } catch {
      await loadTasks();
    }
  }

  return (
    <GlassCard className="animate-fade-in-up opacity-0 flex flex-col flex-1 min-h-0 overflow-x-hidden" style={{ animationDelay: "0.15s" }}>
      <div className="flex items-center justify-between mb-4 shrink-0">
        <h2 className="text-xl font-semibold text-white/90">
          Today&apos;s Tasks
        </h2>
        {canEditTasks && (
          <Link
            href="/todays-tasks"
            className="text-[0.8125rem] font-medium text-white/60 transition-all duration-300 ease-out hover:scale-[1.02] hover:text-blue-300"
          >
            View all →
          </Link>
        )}
      </div>
      {loading ? (
        <p className="text-[0.8125rem] text-white/45">Loading…</p>
      ) : tasks.length === 0 ? (
        <p className="text-[0.8125rem] text-white/45">No tasks for today. Add some in Today&apos;s Tasks.</p>
      ) : (
        <div className="min-h-0 flex flex-col flex-1 overflow-x-hidden" style={{ minHeight: 0 }}>
          <DndContext
            sensors={sensors}
            collisionDetection={closestCenter}
            modifiers={[restrictToVerticalAxis]}
            onDragStart={handleDragStart}
            onDragEnd={handleDragEnd}
            onDragCancel={() => { document.body.style.overflowX = ""; }}
          >
            <ul
              className="space-y-2.5 overflow-y-auto overflow-x-hidden flex-1 min-h-0 no-scrollbar pb-2 w-full"
            >
              <SortableContext
                items={tasksByOrder(tasks).filter((t) => t.status !== "completed").map((t) => t.id)}
                strategy={verticalListSortingStrategy}
              >
                {tasksByOrder(tasks).map((t) =>
                  t.status !== "completed" ? (
                    <SortableTaskRow
                      key={t.id}
                      task={t}
                      isHolding={holdingTaskId === t.id}
                      taskCardStyle={taskCardStyle}
                      onPressStart={(e) => handlePressStart(t, e)}
                      onPressEnd={handlePressEnd}
                      onClick={(e) => handleTaskClick(t, e)}
                      onContextMenu={(e) => e.preventDefault()}
                      readOnly={readOnly}
                      canReorder={canReorder}
                      simplified={simplified}
                    />
                  ) : (
                    <li key={t.id}>
                      <button
                        type="button"
                        onClick={(e) => handleTaskClick(t, e)}
                        onMouseDown={(e) => !readOnly && handlePressStart(t, e)}
                        onMouseUp={handlePressEnd}
                        onMouseLeave={handlePressEnd}
                        onTouchStart={(e) => !readOnly && handlePressStart(t, e)}
                        onTouchEnd={handlePressEnd}
                        onContextMenu={(e) => e.preventDefault()}
                        className={`w-full flex items-center gap-4 px-4 py-3 text-left rounded-[28px] border border-white/[0.12] backdrop-blur-[18px] transition-all duration-300 ease-out ${simplified ? "min-h-[52px] py-4" : ""}`}
                        style={{
                          ...taskCardStyle,
                          opacity: 0.4,
                          transform: "scale(0.98)",
                        }}
                      >
                        <span className="shrink-0 text-xs tabular-nums opacity-70 min-w-[7rem]">
                          {formatTaskTimeRange(t.startTime, t.endTime ?? new Date(new Date(t.startTime).getTime() + (t.durationMinutes ?? 60) * 60000).toISOString(), t.durationMinutes)}
                        </span>
                        <span className="min-w-0 flex-1 truncate text-[0.875rem] text-white/90 line-through">{t.title}</span>
                      </button>
                    </li>
                  )
                )}
              </SortableContext>
            </ul>
          </DndContext>
        </div>
      )}

      {selectedTask && (
        <TaskActionModal
          task={selectedTask}
          onClose={() => { setSelectedTask(null); setIsLongPress(false); }}
          onSkip={() => openSkipModal(selectedTask)}
          onEdit={() => { setEditTask(selectedTask); setSelectedTask(null); setIsLongPress(false); }}
          onHowTo={() => handleHowTo(selectedTask)}
          canEditTasks={canEditTasks}
        />
      )}
      {skipTaskModal && !replacePicker && (
        <SkipTaskModal
          taskTitle={skipTaskModal.title}
          onChoose={handleSkipChoice}
          onClose={() => setSkipTaskModal(null)}
          onEdit={canEditTasks ? () => { setEditTask(skipTaskModal); setSkipTaskModal(null); } : undefined}
        />
      )}
      {skipTaskModal && replacePicker && (
        <ReplaceTaskPickerModal
          skipTask={skipTaskModal}
          candidates={tasks.filter((t) => t.id !== skipTaskModal.id && t.status !== "completed")}
          onSelect={(t) => handleReplaceWith(t)}
          onBack={() => setReplacePicker(false)}
          onClose={() => { setSkipTaskModal(null); setReplacePicker(false); }}
        />
      )}
      {editTask && (
        <EditTaskModal
          task={editTask}
          onClose={() => { setEditTask(null); setSelectedTask(null); }}
          onSave={(payload) => handleEditSave(editTask, payload)}
        />
      )}
      {howToTitle && (
        <HowToPlaceholderModal taskTitle={howToTitle} onClose={() => setHowToTitle(null)} />
      )}
    </GlassCard>
  );
}
